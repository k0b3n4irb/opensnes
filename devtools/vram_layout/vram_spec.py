"""Pure-stdlib VRAM spec / header handling + validation (NO ortools).

Shared by:
  - vram_layout.py  (adds CP-SAT placement on top — the opt-in generator),
  - check_vram_layout.py  (the build gate — validates the committed vram_map.h
    against vram.spec WITHOUT needing ortools).

A `vram.spec` is a tiny per-example manifest, one region per line:

    # name        kind      size_words
    obj_tiles     obj       0x20
    bg_tilemap    bg_map    0x400
    bg_tiles      bg_char   0x10

`kind` drives the hardware alignment the SDK programs (see background.c /
sprite.h). The generator emits a `vram_map.h` of `#define VRAM_<NAME> 0x<base>`
the example includes; the gate reparses both and checks the committed bases are
aligned, non-overlapping, and fit in 64 KB.
"""
from __future__ import annotations

import re
from dataclasses import dataclass
from pathlib import Path

VRAM_WORDS = 0x8000  # 64 KB / 2 bytes per word
ALIGN = {"bg_char": 0x1000, "bg_map": 0x400, "obj": 0x2000}


@dataclass(frozen=True)
class Region:
    name: str
    kind: str
    size: int  # words

    @property
    def align(self) -> int:
        return ALIGN[self.kind]

    @property
    def define(self) -> str:
        return "VRAM_" + self.name.upper()


def parse_spec(path) -> list[Region]:
    regions = []
    for raw in Path(path).read_text().splitlines():
        line = raw.split("#", 1)[0].strip()
        if not line:
            continue
        parts = line.split()
        if len(parts) != 3:
            raise ValueError(f"{path}: expected 'name kind size', got {raw!r}")
        name, kind, size = parts
        if kind not in ALIGN:
            raise ValueError(f"{path}: unknown kind {kind!r} (one of {sorted(ALIGN)})")
        regions.append(Region(name, kind, int(size, 0)))
    return regions


_DEF_RE = re.compile(r"^\s*#define\s+(VRAM_\w+)\s+(0[xX][0-9A-Fa-f]+|\d+)", re.M)


def parse_header(path) -> dict[str, int]:
    return {m.group(1): int(m.group(2), 0) for m in _DEF_RE.finditer(Path(path).read_text())}


def validate(layout: dict[str, int], regions: list[Region]):
    """layout = {define: base}. Returns (ok, [issues])."""
    issues = []
    for r in regions:
        if r.define not in layout:
            issues.append(f"{r.define} missing from header")
            continue
        b = layout[r.define]
        if b % r.align:
            issues.append(f"{r.define}: base 0x{b:04X} not aligned to 0x{r.align:X}")
        if b + r.size > VRAM_WORDS:
            issues.append(f"{r.define}: 0x{b:04X}+0x{r.size:X} overruns VRAM")
    placed = sorted((layout[r.define], layout[r.define] + r.size, r.name)
                    for r in regions if r.define in layout)
    for (a0, a1, an), (b0, b1, bn) in zip(placed, placed[1:]):
        if a1 > b0:
            issues.append(f"overlap: {an}(0x{a0:04X}-0x{a1:04X}) ∩ {bn}(0x{b0:04X}-0x{b1:04X})")
    return (not issues), issues


def emit_header(path, bases: dict[str, int], regions: list[Region], spec_name="vram.spec"):
    """Write a vram_map.h. bases = {region.name: base}."""
    width = max(len(r.define) for r in regions)
    lines = [
        "/* GENERATED by devtools/vram_layout/vram_layout.py --emit — DO NOT EDIT.",
        f" * Source: {spec_name}. Regenerate after editing the spec.",
        " * Validated at build by `make lint-vram` (alignment + no overlap + fit). */",
        "#ifndef VRAM_MAP_H",
        "#define VRAM_MAP_H",
        "",
    ]
    for r in sorted(regions, key=lambda r: bases[r.name]):
        lines.append(f"#define {r.define:<{width}}  0x{bases[r.name]:04X}"
                     f"  /* {r.kind}, {r.size} words */")
    lines += ["", "#endif /* VRAM_MAP_H */", ""]
    Path(path).write_text("\n".join(lines))
