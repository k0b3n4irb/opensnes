/**
 * @file main.c
 * @brief Metasprite composition with switchable OBJ size modes
 * @ingroup examples
 *
 * Demonstrates metasprites: large characters built from multiple hardware
 * OAM entries using oamDrawMeta(). The SNES OBJSEL register ($2101)
 * provides two sprite sizes (small and large) that all 128 OAM entries
 * share. This example lets the user switch between three size combinations
 * to see how the same character is composed from differently-sized pieces.
 *
 * Three sprite sheets (8x8, 16x16, 32x32) are pre-loaded to contiguous
 * VRAM. The gfx4snes -T flag transposes the sprite sheet tiles into SNES
 * OBJ VRAM layout, and the -P flag generates metasprite frame definitions
 * (tile offsets + positions) included as C arrays.
 *
 * When the OBJ size mode changes, OBJSEL and OAM must be updated atomically
 * within the same VBlank to avoid one frame of garbled sprites.
 *
 * @par SNES Concepts
 * - OBJSEL ($2101) size modes: 6 combinations of small/large sprite sizes
 * - Metasprite rendering: composing large characters from multiple OAM entries
 * - Atomic OBJSEL + OAM update within a single VBlank
 * - Transposed sprite sheet layout for SNES OBJ VRAM (gfx4snes -T)
 *
 * @par What to Observe
 * - Two characters displayed: one large, one small
 * - Press UP/DOWN to switch between 3 OBJ size modes
 * - The on-screen text menu shows the current small/large sizes
 * - Observe how the same character is recomposed with different tile sizes
 *
 * @par Modules Used
 * console, sprite, dma, text, text4bpp, background, input
 *
 * @see sprite.h, dma.h, input.h
 */

#include <snes.h>

/** @brief 4bpp tile data for the 32x32 hero sprite (defined in data.asm) */
extern u8 spritehero32_til[];
/** @brief Shared palette for all three hero sprite sizes */
extern u8 spritehero32_pal[];
/** @brief 4bpp tile data for the 16x16 hero sprite */
extern u8 spritehero16_til[];
/** @brief 4bpp tile data for the 8x8 hero sprite */
extern u8 spritehero8_til[];

/**
 * @brief Metasprite frame definitions generated by gfx4snes -P.
 *
 * Each .inc file defines arrays (e.g., hero8_frame0[]) that describe how
 * to compose a character from multiple OAM entries. Each entry in the array
 * contains the relative X/Y offset and tile number for one hardware sprite.
 * oamDrawMeta() reads these arrays to populate OAM.
 */
#include "res/hero8_meta.inc"
#include "res/hero16_meta.inc"
#include "res/hero32_meta.inc"
#include "vram_map.h"  /* generated VRAM bases (devtools/vram_layout) */

/*
 * VRAM Layout — all three sprite sizes are loaded contiguously:
 *   0x0000: hero32 tiles (192 tiles = 3072 words = 6144 bytes)
 *   0x0C00: hero16 tiles (96 tiles = 1536 words = 3072 bytes)
 *   0x1200: hero8 tiles  (16 tiles = 256 words = 512 bytes)
 *   0x4000: BG1 font tiles (4bpp, 8KB-aligned for bgSetGfxPtr)
 *   0x6800: BG1 text tilemap
 *
 * OBJSEL base = $0000, name_select = 0 (second name page at $1000).
 * All sprite tiles are addressable as OAM tile numbers 0-303.
 */

/* VRAM_SPRITES / VRAM_FONT / VRAM_TEXT_MAP come from the generated vram_map.h.
 * The three hero sub-sheets are laid out at fixed offsets inside the sprite
 * region (the solver places the region base; sub-sheet packing stays manual). */
/** @brief VRAM word address for 32x32 hero tiles */
#define VRAM_HERO32   VRAM_SPRITES
/** @brief VRAM word address for 16x16 hero tiles (after 32x32 tiles) */
#define VRAM_HERO16   (VRAM_SPRITES + 0x0C00)
/** @brief VRAM word address for 8x8 hero tiles (after 16x16 tiles) */
#define VRAM_HERO8    (VRAM_SPRITES + 0x1200)

/** @brief Number of tiles in the 32x32 hero sprite sheet */
#define HERO32_TILES  192
/** @brief Number of tiles in the 16x16 hero sprite sheet */
#define HERO16_TILES  96
/** @brief Number of tiles in the 8x8 hero sprite sheet */
#define HERO8_TILES   16
/** @brief Bytes per 4bpp 8x8 tile (8 rows x 4 bits/pixel x 8 pixels / 8 bits = 32) */
#define TILE_BYTES    32

/**
 * @brief Base tile numbers for oamDrawMeta().
 *
 * oamDrawMeta() adds the baseTile offset to each metasprite entry's relative
 * tile number to compute the final OAM tile index. Since all three sprite
 * sheets are loaded contiguously in VRAM, each sheet's base tile is the
 * cumulative tile count of all preceding sheets.
 */
#define BASE_TILE_32  0                                  /**< First tile of 32x32 sheet */
#define BASE_TILE_16  (HERO32_TILES)                     /**< First tile of 16x16 sheet (192) */
#define BASE_TILE_8   (HERO32_TILES + HERO16_TILES)      /**< First tile of 8x8 sheet (288) */

/** @brief Currently selected OBJ size mode index (0-2), controlled by UP/DOWN */
u16 selectedItem;

static void drawSprites(void);

/**
 * @brief Redraw the on-screen menu showing all three OBJ size modes.
 *
 * Prints the three available size combinations with a ">" cursor next to
 * the currently selected mode. The text is rendered to a BG1 tilemap buffer
 * and DMAed to VRAM by the NMI handler during the next VBlank.
 */
static void drawMenu(void) {
    textClearRect(3, 2, 28, 4);
    textPrintAt(3, 2, "Object size :");
    if (selectedItem == 0) {
        textPrintAt(3, 3, "> Small:  8 - Large: 16");
    } else {
        textPrintAt(3, 3, "  Small:  8 - Large: 16");
    }
    if (selectedItem == 1) {
        textPrintAt(3, 4, "> Small:  8 - Large: 32");
    } else {
        textPrintAt(3, 4, "  Small:  8 - Large: 32");
    }
    if (selectedItem == 2) {
        textPrintAt(3, 5, "> Small: 16 - Large: 32");
    } else {
        textPrintAt(3, 5, "  Small: 16 - Large: 32");
    }
}

/**
 * @brief Atomically switch OBJSEL size mode and update OAM in one VBlank.
 *
 * Changing OBJSEL and OAM must happen in the same VBlank frame. If OBJSEL
 * changes in one VBlank but OAM still contains tile numbers calculated for
 * the old size mode, the PPU interprets the tiles with the wrong dimensions
 * for one frame, causing visible sprite garbling.
 *
 * The "fresh sync" pattern clears vblank_flag after preparing the OAM buffer,
 * ensuring WaitForVBlank() waits for a NEW NMI (not a stale flag from an NMI
 * that may have fired mid-preparation and DMA'd a partial buffer).
 */
static void changeObjSize(void) {
    /* Prepare new OAM data BEFORE VBlank so the NMI DMA sends correct
     * sprites in the same VBlank where OBJSEL changes. */
    oamClear();
    drawSprites();

    /* Fresh sync: clear the flag so WaitForVBlank waits for a new NMI,
     * not a stale one that may have DMA'd a partially-built OAM buffer. */
    vblank_flag = 0;

    WaitForVBlank();
    /* Write OBJSEL AFTER NMI has DMA'd the new OAM buffer.
     * This is still within VBlank, so the PPU sees both changes atomically. */
    if (selectedItem == 0)
        REG_OBJSEL = OBJ_SIZE_TO_REG(OBJ_SIZE8_L16);
    else if (selectedItem == 1)
        REG_OBJSEL = OBJ_SIZE_TO_REG(OBJ_SIZE8_L32);
    else
        REG_OBJSEL = OBJ_SIZE_TO_REG(OBJ_SIZE16_L32);
}

/**
 * @brief Render metasprites using oamDrawMeta() for the current size mode.
 *
 * oamDrawMeta() reads metasprite frame data (tile offsets + positions) and
 * populates multiple OAM entries to compose a large character from smaller
 * hardware sprite pieces. It returns the next available OAM ID, allowing
 * multiple metasprites to be drawn without ID conflicts.
 *
 * Each size mode uses a different combination of LARGE and SMALL sprites
 * to render two characters: one larger (left) and one smaller (right).
 */
static void drawSprites(void) {
    u8 nextId;

    if (selectedItem == 0) {
        /* Mode 0: hero16 as LARGE (16x16) + hero8 as SMALL (8x8) */
        nextId = oamDrawMeta(0, 64, 140, hero16_frame0,
                             BASE_TILE_16, 0, OBJ_LARGE);
        oamDrawMeta(nextId, 160, 148, hero8_frame0,
                    BASE_TILE_8, 0, OBJ_SMALL);
    } else if (selectedItem == 1) {
        /* Mode 1: hero32 as LARGE (32x32) + hero8 as SMALL (8x8) */
        nextId = oamDrawMeta(0, 48, 108, hero32_frame0,
                             BASE_TILE_32, 0, OBJ_LARGE);
        oamDrawMeta(nextId, 160, 148, hero8_frame0,
                    BASE_TILE_8, 0, OBJ_SMALL);
    } else {
        /* Mode 2: hero32 as LARGE (32x32) + hero16 as SMALL (16x16) */
        nextId = oamDrawMeta(0, 48, 108, hero32_frame0,
                             BASE_TILE_32, 0, OBJ_LARGE);
        oamDrawMeta(nextId, 160, 124, hero16_frame0,
                    BASE_TILE_16, 0, OBJ_SMALL);
    }
}

/**
 * @brief Entry point: metasprite demo with switchable OBJ size modes.
 *
 * Sets up a text menu on BG1, loads three sizes of hero sprite tiles to
 * contiguous OBJ VRAM, and lets the user switch between three OBJSEL size
 * combinations to see how metasprites are composed from differently-sized
 * hardware sprite pieces.
 *
 * @return Never returns (infinite loop).
 */
int main(void) {
    consoleInit();
    setMode(BG_MODE1, 0);

    /* Move BG1 tile/map pointers to high VRAM addresses so they don't
     * overlap with the OBJ sprite tiles at $0000-$1300. */
    bgSetGfxPtr(0, VRAM_FONT);
    bgSetMapPtr(0, VRAM_TEXT_MAP, SC_32x32);

    /* Load font for text on BG1 (4bpp in Mode 1) */
    textLoadFont4bpp(VRAM_FONT);
    /* textInitEx takes byte address (word addr * 2), font_tile=0 (font at BG base) */
    textInit(VRAM_TEXT_MAP * 2, 0, 0);

    /* Font palette: BG palette 0, color 0=black, color 1=white */
    setColor(0, RGB(0, 0, 0));
    setColor(1, RGB(31, 31, 31));

    /* Load all sprite tile data to OBJ VRAM (screen still blanked) */
    dmaCopyVram(spritehero32_til, VRAM_HERO32, HERO32_TILES * TILE_BYTES);
    dmaCopyVram(spritehero16_til, VRAM_HERO16, HERO16_TILES * TILE_BYTES);
    dmaCopyVram(spritehero8_til,  VRAM_HERO8,  HERO8_TILES * TILE_BYTES);

    /* Load sprite palette (sprite palette 0 = CGRAM $80) */
    dmaCopyCGram(spritehero32_pal, OBJ_CGRAM_BASE, PALETTE_16_SIZE);

    /* Initialize OBJ settings */
    selectedItem = 0;
    REG_OBJSEL = OBJ_SIZE_TO_REG(OBJ_SIZE8_L16);
    oamClear();

    /* Enable BG1 (text) and OBJ (sprites) */
    setMainScreen(TM_BG1 | TM_OBJ);

    drawMenu();
    drawSprites();
    WaitForVBlank();
    setScreenOn();

    while (1) {
        u16 pressed;
        pressed = padPressed(0);

        if (pressed & KEY_UP) {
            if (selectedItem > 0) {
                selectedItem--;
                drawMenu();
                changeObjSize();
            }
        }
        if (pressed & KEY_DOWN) {
            if (selectedItem < 2) {
                selectedItem++;
                drawMenu();
                changeObjSize();
            }
        }

        drawSprites();
        WaitForVBlank();
    }

    return 0;
}
