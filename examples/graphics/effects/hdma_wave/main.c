/* Direct hardware registers */
#define HW_HDMAEN  (*(vu8*)0x420C)
#define HW_DMAP6   (*(vu8*)0x4360)
#define HW_BBAD6   (*(vu8*)0x4361)
#define HW_A1T6L   (*(vu8*)0x4362)
#define HW_A1T6H   (*(vu8*)0x4363)
#define HW_A1B6    (*(vu8*)0x4364)
/* State variables (static = placed in RAM by compiler) */
static u8 wave_on;
static u8 amp_idx;
static u8 animating;
static u8 phase;

/* Pre-computed offsets to avoid runtime multiplication */
static const u16 amp_offsets[AMP_LEVELS] = {
    0, 1006, 2012, 3018, 4024, 5030, 6036
};

int main(void) {
    u16 i;
    u16 tbl_addr;
    u16 phase_off;

    /* Init state */
    wave_on = 0;
    amp_idx = AMP_DEFAULT;
    animating = 0;
    phase = 0;

    consoleInit();
    setMode(1, 0);

    /* Upload 2 tiles (4bpp) to VRAM at word address 0x0000 */
    *(vu8*)0x2115 = 0x80;
    *(vu8*)0x2116 = 0x00;
    *(vu8*)0x2117 = 0x00;
    for (i = 0; i < 32; i++) {
        *(vu8*)0x2118 = tiles[i];
        *(vu8*)0x2119 = 0x00;
    }

    /* Palette: color 0 = black, color 1 = white */
    *(vu8*)0x2121 = 0;
    *(vu8*)0x2122 = 0x00;
    *(vu8*)0x2122 = 0x00;
    *(vu8*)0x2122 = 0xFF;
    *(vu8*)0x2122 = 0x7F;

    /* Fill BG1 tilemap at VRAM word address 0x0400 */
    *(vu8*)0x2115 = 0x80;
    *(vu8*)0x2116 = 0x00;
    *(vu8*)0x2117 = 0x04;
    for (i = 0; i < 1024; i++) {
        if (i & 1) {
            *(vu8*)0x2118 = 0x01;
        } else {
            *(vu8*)0x2118 = 0x00;
        }
        *(vu8*)0x2119 = 0x00;
    }

    /* Configure HDMA channel 6 */
    HW_DMAP6 = 0x02;    /* mode 2: write twice to same register */
    HW_BBAD6 = 0x0D;    /* target: BG1HOFS ($210D) */
    HW_A1B6  = 0x00;    /* bank 0 (ROM) */
    HW_HDMAEN = 0x00;   /* start with HDMA off */

    setScreenOn();

    while (1) {
        WaitForVBlank();

        /* A button: toggle wave on/off */
        if (padPressed(0) & KEY_A) {
            if (wave_on == 0) {
                wave_on = 1;
            } else {
                wave_on = 0;
                animating = 0;
                phase = 0;
            }
        }

        /* D-pad RIGHT: increase amplitude */
        if (padPressed(0) & KEY_RIGHT) {
            if (amp_idx < 6) {
                amp_idx = amp_idx + 1;
            }
        }

        /* D-pad LEFT: decrease amplitude */
        if (padPressed(0) & KEY_LEFT) {
            if (amp_idx > 0) {
                amp_idx = amp_idx - 1;
            }
        }

        /* D-pad UP: start animation */
        if (padPressed(0) & KEY_UP) {
            animating = 1;
        }

        /* D-pad DOWN: stop animation (freeze at current phase) */
        if (padPressed(0) & KEY_DOWN) {
            animating = 0;
        }

        /* Advance animation phase */
        if (animating) {
            phase = phase + 1;
            if (phase >= 112) {
                phase = 0;
            }
        }

        /* Update HDMA table pointer every frame */
        if (wave_on) {
            phase_off = (u16)phase * 3;
            tbl_addr = (u16)hdma_tables + amp_offsets[amp_idx] + phase_off;
            HW_A1T6L = (u8)(tbl_addr & 0xFF);
            HW_A1T6H = (u8)((tbl_addr >> 8) & 0xFF);
            HW_HDMAEN = 0x40;
        } else {
            HW_HDMAEN = 0x00;
        }
    }
    return 0;
}
